package JavaStudy01;

public class Java19_1 {
	public void sum() {
		int x = 10;
		int y = 20;
		int result = x + y;
		System.out.println(" 두 수의 덧셈 : " + result);	
	}
	
	public void sum(int x, int y) {
		int result = x + y;
		System.out.println(" 두 수의 덧셈 : " + result);
	}
	
	public void sum(double x, double y) {
		double result = x + y;
		System.out.println(" 두 수의 덧셈 : " + result);
	}
}
