package JavaStudy01;

public class Java19_2 {
	public void sub() {
		int x = 30;
		int y = 20;
		int result = x-y;
		System.out.println("두 수의 덧셈 : " + result);
	}
	public void sub(int x, int y) {
		int result = x-y;
		System.out.println("두 수의 덧셈 : " + result);
	}
	public void sub(double x, double y) {
		double result = x-y;
		System.out.println("두 수의 덧셈 : " + result);
	}
	public void sub(int x) {
		int y = 20;
		int result = x-y;
		System.out.println("두 수의 덧셈 : " + result);
	}
	public void sub(int x, double y) {
		double result = x - y;
		System.out.println("두 수의 덧셈 : " + result);		
	}
}
